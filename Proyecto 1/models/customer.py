class Customer:
    def __init__(self, id, name, lastname, address, phone, creditCard, age, gender, salary, country):
        self.id = id
        self.name = name
        self.lastname = lastname
        self.address = address
        self.phone = phone
        self.creditCard = creditCard
        self.age = age
        self.gender = gender
        self.salary = salary
        self.country = country
